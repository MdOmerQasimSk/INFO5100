    private void jPatientDetailsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPatientDetailsMouseClicked
        // TODO add your handling code here:
        int selectedRow = jPatientDetails.getSelectedRow();
        if(selectedRow<0)
        {
            return;
        }
        
        jBodyWeightTxt.setEnabled(true);
        jHeightTxt.setEnabled(true);
        jBloodPressureTxt.setEnabled(true);
        jTemperatureTxt.setEnabled(true);
        jBtnUpdateVitals.setEnabled(true);
        
        DefaultTableModel ptmodel = (DefaultTableModel) jPatientDetails.getModel();
        Patient selectedRowObj = (Patient) ptmodel.getValueAt(selectedRow, 0);
        
        //Display rowData
        jPatientID.setText(String.valueOf(selectedRowObj.getPatientId()));
        jPatientName.setText(selectedRowObj.getPatientName());
        jPatientAge.setText(String.valueOf(selectedRowObj.getPatientAge()));
        jPatientCity.setText(String.valueOf(selectedRowObj.getCity()));
        jPatientCommunity.setText(String.valueOf(selectedRowObj.getCommunity()));
        jPatientHospital.setText(String.valueOf(selectedRowObj.getHospital()));
        jPatientTelNum.setText(selectedRowObj.getPatientTelNum());
        jPatientEmail.setText(selectedRowObj.getPatientEmail()); 
    
    }//GEN-LAST:event_jPatientDetailsMouseClicked

    private void jBtnUpdateVitalsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnUpdateVitalsActionPerformed
        // TODO add your handling code here:

        //valildations
        boolean checkIsValid = validateWeight() && validateHeight() && validateTemperature() && validatePressure();

        if(!checkIsValid){
            JOptionPane.showMessageDialog(this, "Please enter required fields!");
            return;
        }
        
        VitalSign vitalSign = new VitalSign();
        
        vitalSign.setBodyWeight(Double.parseDouble(jBodyWeightTxt.getText()));
        vitalSign.setHeight(Double.parseDouble(jHeightTxt.getText()));
        vitalSign.setTemperature(Double.parseDouble(jTemperatureTxt.getText()));
        vitalSign.setBloodPressure(jBloodPressureTxt.getText());
        populateVitalDataInPatient(vitalSign);
        populateVitalTable();
        clearFeilds();
        refreshTable();
        jBodyWeightTxt.setText("");
        jHeightTxt.setText("");
        jTemperatureTxt.setText("");
        jBloodPressureTxt.setText("");
        
        jBodyWeightTxt.setEnabled(false);
        jHeightTxt.setEnabled(false);
        jTemperatureTxt.setEnabled(false);
        jBloodPressureTxt.setEnabled(false);
        jBtnUpdateVitals.setEnabled(false);
        
    }//GEN-LAST:event_jBtnUpdateVitalsActionPerformed

