    private void refreshTable() {
    DefaultTableModel dtmodel = (DefaultTableModel) jPatientDetails.getModel();
        dtmodel.setRowCount(0);
        
        
        //Populate rowData
        ArrayList<Patient>dPList = new ArrayList <Patient>();
        {
           for(Patient p:patientCatalogue.getPatientList())
           {
               if(jDoctorCombo.getSelectedItem().toString().equals(p.getConsultedDoctor()) && p.getVitalSign()==null )
               {
                   dPList.add(p);
               }
           }
        }
        for(Patient p: dPList){
                Object[] obj = new Object[6];
                obj[0] = p;
                obj[1] = p.getPatientName();
                obj[2] = p.getPatientAge();
                obj[3] = p.getCity();
                obj[4] = p.getCommunity();
                obj[5] = p.getHospital();
                dtmodel.addRow(obj);
            }
            
    }

    
    private void populateVitalDataInPatient(VitalSign vitalSign){
        for(Patient np: patientCatalogue.getPatientList())
        { 
            int id=Integer.parseInt(jPatientID.getText());
            if(np.getPatientId()==id)
            {
              np.setVitalSign(vitalSign);
            }
        }
    }
    
    private void populateVitalTable() {
        DefaultTableModel dtmodel = (DefaultTableModel) jVitals.getModel();
        dtmodel.setRowCount(0);
        //Populate rowData
        ArrayList<Patient> tempList = patientCatalogue.getPatientList(); //model-class call
            //Iterate and show data
            
            for(Patient p: tempList){
                if(p.getVitalSign()!=null && p.getConsultedDoctor().equalsIgnoreCase(jDoctorCombo.getSelectedItem().toString()))
                {
                Object[] obj = new Object[6];
                obj[0] = p;
                obj[1] = p.getPatientName();
                obj[2] = p.getVitalSign().getBodyWeight();
                obj[3] = p.getVitalSign().getHeight();
                obj[4] = p.getVitalSign().getTemperature();
                obj[5]=  p.getVitalSign().getBloodPressure();

                dtmodel.addRow(obj);
            }
            }
            
        
        }

